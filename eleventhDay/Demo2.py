# 验证身份证号是否正确  18位，前17位是数字，后一位是数字或者是x
import re

result = re.match(r"^\d{6}((20[012][01234])|(1[89]\d\d))([\d|X])$","123456789012345678")

# 匹配20年份 20xx
a = re.match(r"^20[012][01234]", "2024")
print(a)

# 匹配19/18年份
b = re.match(r"^1[89]\d\d", "1982")
print(b)

# 匹配生日

c = re.match(r"^0[123456789][0123][01]", "0128")
print(c)


# 手机号码验证

d = re.match(r"^1\d{10}", "12345678901")
print(d)


